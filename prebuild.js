function toBase64(input) {
  return Buffer.from(input, 'utf-8').toString('base64');
}

function reformat(value) {
  if (value === undefined) {
    return value;
  }
  return `'${value}'`;
}

function isBoolean(value) {
  return value === 'true' || value === 'false';
}

fs.readFile('./src/environment.js.tmpl', 'utf8', function (err, data) {
  if (err) {
    throw err;
  }

  data = data
    .replace('@TITLE@', reformat(process.env.TITLE ? process.env.TITLE : ''))
    .replace('@LOCALE@', reformat(process.env.LOCALE ? process.env.LOCALE : 'de-DE'))
    .replace('@ADDRESSING@', reformat(process.env.ADDRESSING ? process.env.ADDRESSING : 'du'))
    .replace('@API_URL@', reformat(process.env.API_URL))
    .replace(
      '@CUSTOMER_ID@',
      reformat(process.env.CUSTOMER_ID ? process.env.CUSTOMER_ID : '11111111-1111-1111-1111-111111111111')
    )
    .replace('@EMAIL@', reformat(toBase64(process.env.EMAIL ? process.env.EMAIL : 'demo@example.com')))
    .replace('@COLORS@', reformat(process.env.COLORS ? process.env.COLORS : undefined))
    .replace('@IMPRINT@', reformat(toBase64(process.env.IMPRINT ? process.env.IMPRINT : `${imprint}`)))
    .replace('@PRIVACY@', reformat(toBase64(process.env.PRIVACY ? process.env.PRIVACY : `${privacy}`)))
    .replace('@TOS@', reformat(toBase64(process.env.TOS ? process.env.TOS : `${tos}`)))
    .replace(
      '@ACCESSIBILITY@',
      reformat(toBase64(process.env.ACCESSIBILITY ? process.env.ACCESSIBILITY : `${accessibility}`))
    );

  data = data
    .replace('@GENERATED_COMMENT_TS@', '// AUTO GENERATED - DO NOT EDIT THIS FILE')
    .replace('@VERSION@', reformat(process.env.VERSION ? process.env.VERSION : packageJson.version))
    .replace('@BUILD_DATE@', reformat(new Date().toISOString()))
    .replace('@SURVEY_LINK_ADMIN@', reformat(process.env.SURVEY_LINK_ADMIN ? process.env.SURVEY_LINK_ADMIN : undefined))
    .replace(
      '@SURVEY_LINK_USER@',
      reformat(process.env.SURVEY_SURVEY_LINK_USER ? process.env.SURVEY_SURVEY_LINK_USER : undefined)
    )
    .replace('@SHOW_REFERENCE@', isBoolean(process.env.SHOW_REFERENCE) ? process.env.SHOW_REFERENCE : 'true')
    .replace('@API_REQUEST_TIMEOUT_IN_MS@', '20000')
    .replace('@CONSOLE_LOGGING_OPTIONS@', `{momentDateTimeFormat: 'YYYY-MM-DD HH:mm:ss.SSS', logLevelThreshold: 5}`)
    .replace(
      '@API_MEDIA_TYPE@',
      reformat(process.env.API_MEDIA_TYPE ? process.env.API_MEDIA_TYPE : 'application/terminfinder.api-v1+json')
    );

  fs.writeFile('./src/environment.js', data, 'utf8', function (err) {
    if (err) {
      throw err;
    }
  });
});

